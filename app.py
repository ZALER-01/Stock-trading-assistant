        "    print(f\"Support Level: {support}\")\n",
        "    print(f\"Target Price: {target}\")\n",
        "    print(f\"Stop Loss: {stop_loss}\")\n",
        "    print(f\"Signal: {signal}\")\n",
        "\n",
        "    # =========================\n",
        "    # 💡 SUGGESTION\n",
        "    # =========================\n",
        "    print(\"\\n💡 SUGGESTION:\")\n",
        "\n",
        "    if signal == \"🟢 BUY\":\n",
        "        print(f\"Strong stock. Target: {target}, Stop Loss: {stop_loss}\")\n",
        "\n",
        "    elif signal == \"🔴 SELL\":\n",
        "        print(\"Weak trend. Consider exiting or reducing position.\")\n",
        "\n",
        "    else:\n",
        "        if latest['RSI'].item() < 35:\n",
        "            print(f\"Oversold zone. Possible bounce.\\nTarget: {target}, Stop Loss: {stop_loss}\")\n",
        "        else:\n",
        "            print(\"Hold and wait for better opportunity.\")\n",
        "\n",
        "# =========================\n",
